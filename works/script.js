// Function to display packaging
function displayPackaging() {
  const selectedProductName = productNameSelect.value;
  const lensType = lensTypeSelect.value;
  const lensData = data[lensType];
  const selectedProduct = lensData.find((product) => product.Product_Name === selectedProductName);


  // Clear packaging options
  packagingSelect.innerHTML= '';

  // Add packaging options

  if(selectedProduct){
    const option = document.createElement('option');
    option.value = '';
    option.text = 'Select Packaging';
    packagingSelect.appendChild(option);
    selectedProduct.Packaging.forEach((packages)=>{
      const option = document.createElement('option');
      option.value = packages;
      option.text = packages;
      packagingSelect.appendChild(option);
    })
  }
}



